public class Contact
{
    //==============================================================================================
    // Class Properties
    //==============================================================================================

    private String name;
    private long gid;

    //==============================================================================================
    // Constructor
    //==============================================================================================

    public Contact(String name, long gid)
    {
        this.name = name;
        this.gid = gid;
    }

    //==============================================================================================
    // Class Instance Methods
    //==============================================================================================

    public String getName()
    {
        return name;
    }

    public long getGid()
    {
        return gid;
    }
}
